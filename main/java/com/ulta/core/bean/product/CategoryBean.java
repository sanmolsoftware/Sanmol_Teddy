public class CategoryBean extends UltaBean {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2031669071579387223L;

	/** The cat image path value. */
	private String catImagePathValue;

	/** The display name. */
	private String displayName;
	
	/** The id. */
	private String id;

	/**
	 * Gets the cat image path value.
	 *
	 * @return the catImagePathValue
	 */
	public String getCatImagePathValue() {
		return catImagePathValue;
	}

	/**
	 * Sets the cat image path value.
	 *
	 * @param catImagePathValue the catImagePathValue to set
	 */
	public void setCatImagePathValue(String catImagePathValue) {
		this.catImagePathValue = catImagePathValue;
	}

	/**
	 * Gets the display name.
	 *
	 * @return the displayName
	 */
	public String getDisplayName() {
		return displayName;
	}

	/**
	 * Sets the display name.
	 *
	 * @param displayName the displayName to set
	 */
	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}

	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * Sets the id.
	 *
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	
	
	
}
